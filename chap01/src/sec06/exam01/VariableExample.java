package sec06.exam01;

public class VariableExample {
	public static void main(String[] args) {
		/*멀티라인, //는 싱글라인*/
		int a = 1;//변수선언, 초기값=1
		a = 10;//변수 초기화(10:초기값)를 하고 읽어줘야한다.
		
		int value = 10;//type이 한번 정해지면 그것만 저장가능(변수타입 변수명 =)
		int result = value + 12;
		System.out.println(result);//읽기
		
	}
}
